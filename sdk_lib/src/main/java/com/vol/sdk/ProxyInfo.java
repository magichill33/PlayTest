package com.vol.sdk;


import com.vol.sdk.base.BaseInfo;

public class ProxyInfo extends BaseInfo {
	
	private String version;
	private String buildTime;
	private String vliveVersion;
	private String p2pliveVersion;
	private String vooletvVersion;
	private String m3u8Version;
	private String vbrVersion;
	private String downspeed;
	
	public String getDownspeed() {
		return downspeed;
	}
	public void setDownspeed(String downspeed) {
		this.downspeed = downspeed;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getBuildTime() {
		return buildTime;
	}
	public void setBuildTime(String buildTime) {
		this.buildTime = buildTime;
	}
	public String getVliveVersion() {
		return vliveVersion;
	}
	public void setVliveVersion(String vliveVersion) {
		this.vliveVersion = vliveVersion;
	}
	public String getP2pliveVersion() {
		return p2pliveVersion;
	}
	public void setP2pliveVersion(String p2pliveVersion) {
		this.p2pliveVersion = p2pliveVersion;
	}
	public String getVooletvVersion() {
		return vooletvVersion;
	}
	public void setVooletvVersion(String vooletvVersion) {
		this.vooletvVersion = vooletvVersion;
	}
	public String getM3u8Version() {
		return m3u8Version;
	}
	public void setM3u8Version(String m3u8Version) {
		this.m3u8Version = m3u8Version;
	}
	public String getVbrVersion() {
		return vbrVersion;
	}
	public void setVbrVersion(String vbrVersion) {
		this.vbrVersion = vbrVersion;
	}
	
	
	
}
